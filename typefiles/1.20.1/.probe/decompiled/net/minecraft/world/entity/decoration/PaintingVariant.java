package net.minecraft.world.entity.decoration;

public class PaintingVariant {

    private final int width;

    private final int height;

    public PaintingVariant(int int0, int int1) {
        this.width = int0;
        this.height = int1;
    }

    public int getWidth() {
        return this.width;
    }

    public int getHeight() {
        return this.height;
    }
}